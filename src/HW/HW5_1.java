package HW;

public class HW5_1 {
    // сложение 2х чисел
    public int sum(int a, int b) {
        return (a + b);
    }

    // вычитание 2х чисел
    public int sub(int a, int b) {
        return (a - b);
    }

    // умножение 2х чисел
    public int mult(int a, int b) {
        return (a * b);
    }

    // деление 2х чисел
    public double div(double a, double b) {
        return (a / b);
    }

}
